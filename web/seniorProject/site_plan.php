                    <h2 id="desc">Site Description</h2>
                    <div>
                        <p>The series of web pages discussed in this proposal document will be designed for the purpose of
                        satisfying the Capstone Project Proposal requirements at BYU-I. The web pages will be compiled
                        under a single marketing theme to illustrate the web design and development knowledge I have
                        gained and my capacity or skillset to learn new technologies in the web industry. This document
                        outlines the technologies or tools that will be used, web page details, and project timelines.</p>

                        <p>These web pages are intended to be used for an online business built on a drop-shipping business
                        model. The product showcased will be manufactured and shipped by a third party. The sole responsibility of this online business is to build online presence for our brand, market to our target
                        audience, and allow our customers to make purchases through the website.</p>
                        
                        <p>The site will not be published until project completion. At that time, the site will be created under a
                        business name and license, third party relationships secured with World Wide Brands, and a fully functioning shopping cart and payment center implemented.</p>
